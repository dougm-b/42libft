void ft_putnbr_fd(int n, int fd)
{
    int	c;

	c = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	if (nb <= 2147483647 && nb > -2147483648)
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb > 9)
		{
			ft_putnbr(nb / 10);
		}
		c = nb % 10 + '0';
		write (1, &c, 1);
	}
}